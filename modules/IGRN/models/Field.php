class IGRN_Field_Model extends Vtiger_Field_Model
{
	public function isAjaxEditable()
	{
		$edit = parent::isAjaxEditable();
		if ($edit && $this->getName() === 'igrn_status') {
			$edit = false;
		}

		return $edit;
	}
}
